package santander.tecnologia.challenge.ws.weather.response;

public class WeatherDayTemperature {
	
	private float day;
	private float min;
	private float max;
	private float eve;
	private float nigth;
	private float morn;
	
	
	public float getDay() {
		return day;
	}
	public void setDay(float day) {
		this.day = day;
	}
	public float getMin() {
		return min;
	}
	public void setMin(float min) {
		this.min = min;
	}
	public float getMax() {
		return max;
	}
	public void setMax(float max) {
		this.max = max;
	}
	public float getEve() {
		return eve;
	}
	public void setEve(float eve) {
		this.eve = eve;
	}
	public float getNigth() {
		return nigth;
	}
	public void setNigth(float nigth) {
		this.nigth = nigth;
	}
	public float getMorn() {
		return morn;
	}
	public void setMorn(float morn) {
		this.morn = morn;
	}
	
	
	
	

}
